const resolveInitialLanguage = () => {
  const savedLanguage = localStorage.getItem(STORAGE_KEY);
  return savedLanguage === "vi" || savedLanguage === "en" ? savedLanguage : "vi";
};

const interpolate = (template, values = {}) =>
  String(template).replace(/\{(\w+)\}/g, (_, key) => values[key] ?? "");

export function I18nProvider({ children }) {
  const [language, setLanguage] = useState(resolveInitialLanguage);

  const locale = language === "vi" ? "vi-VN" : "en-US";

  const value = useMemo(
    () => ({
      language,
      locale,
      setLanguage: (nextLanguage) => {
        localStorage.setItem(STORAGE_KEY, nextLanguage);
        setLanguage(nextLanguage);
      },
      t: (key, fallback = "", values = {}) => {
        const template = translations[language]?.[key] ?? translations.en[key] ?? fallback ?? key;

        return interpolate(template, values);
      },
      formatDateTime: (value, options = {}) => new Date(value).toLocaleString(locale, options),
    }),
    [language, locale]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export const useI18n = () => {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }

  return context;
};
